print("Exemplo 1 (Sequência de 1 a 100):")
for i in range(100):
    print(i)

print("Exemplo 2 (Sequência de 1 a 100):")
for j in range(1,101):
    print(j)
    
print("Exemplo 3 (Sequência de 1 a 99 - Ímpares):")
for k in range(1,101,2):
    print(k)

print("Exemplo 4 (Sequência de 100 a 1 - Descrescente):")
for l in range(100,0,-1):
    print(l)
    